package com.bookworm.bookworm;

public class desclist {

    public String image1;
    public String image2;
    public String booknames;
    public String bookdesc;
    public String bookprice;
    public String author;
    public String category;
    public String condition;
    public String format;
    public String language;
    public String rent;
    public String tenure;

    public desclist(String image1, String image2, String booknames, String bookdesc, String bookprice, String author, String category, String condition, String format, String language, String rent, String tenure) {
        this.image1 = image1;
        this.image2 = image2;
        this.booknames = booknames;
        this.bookdesc = bookdesc;
        this.bookprice = bookprice;
        this.author = author;
        this.category = category;
        this.condition = condition;
        this.format = format;
        this.language = language;
        this.rent = rent;
        this.tenure = tenure;
    }
}
